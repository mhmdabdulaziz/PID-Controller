/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* MCU Configuration--------------------------------------------------------*/
  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_TIM1_Init();

  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1); // Start the PWM

  // Initialize PID controller parameters
  PID_Controller pidController = {
      .kp = 2.0f,
      .ki = 0.5f,
      .kd = 0.25f,
      .integral_limit_min = -100.0f,
      .integral_limit_min_int = -50.0f,
      .setpoint = 2000.0f,
      .previous_error = 0.0f,
      .integral_term = 0.0f,
	  .current_value = 0.0f
  };

  /* Infinite loop */
  while (1)
  {
	  // Read the current light intensity using the ADC
	  pidController.current_value = read_light_intensity(); // Read the current light intensity from the ADC

	  // Calculate the PID control value based on the current light intensity
	  float pid_output = calculate_PID(&pidController); // Calculate the PID control output

	  // Set the light intensity using the calculated PID control output
	  set_light_intensity(pid_output); // Adjust the light intensity using the PID control output
  }
}

