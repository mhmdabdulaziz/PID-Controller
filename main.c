float calculate_PID(PID_Controller *pid) {
    // Calculate error
    float err = pid->setpoint - pid->current_value;

    // Calculate integral term and limit it to a minimum value
    pid->integral_term += err;
    if (pid->integral_term < pid->integral_limit_min) {
        pid->integral_term = pid->integral_limit_min;
    }

    // Calculate derivative term and limit it to a minimum value
    float derivative = err - pid->previous_error;
    if (derivative < pid->integral_limit_min_int) {
        derivative = pid->integral_limit_min_int;
    }

    // Calculate PID output
    float output = (pid->kp * err) + (pid->ki * pid->integral_term) + (pid->kd * derivative);

    // Ensure the output is non-negative
    if (output < 0) {
        output = 0;
    }

    // Store the current error for the next iteration
    pid->previous_error = err;

    return output;
}

float read_light_intensity(void) {
    float adc_value = 0.0f;

    // Start ADC Conversion
    HAL_ADC_Start(&hadc1);

    // Wait for conversion to complete and read ADC Value
    if (HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY) == HAL_OK) {
        adc_value = HAL_ADC_GetValue(&hadc1);
    }

    // Stop ADC Conversion
    HAL_ADC_Stop(&hadc1);

    return adc_value;
}

void set_light_intensity(uint16_t duty_cycle) {
    // Update the PWM duty cycle value in CCR1 register
    TIM1->CCR1 = duty_cycle;
}


