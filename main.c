/* Private typedef -----------------------------------------------------------*/
typedef struct {
    float kp;        // Proportional gain
    float ki;        // Integral gain
    float kd;        // Derivative gain
    float integral_limit_min; // Minimum limit for the integral term
    float integral_limit_min_int; // Minimum limit for the integral term (derivative)
    float setpoint;  // Desired point
    float previous_error;
    float integral_term; // Accumulated integral term
    float current_value; // Current measured value
} PID_Controller;

/* Private define ------------------------------------------------------------*/

/* Private macro -------------------------------------------------------------*/

